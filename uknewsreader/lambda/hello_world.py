import feedparser
import html2text
import logging
from collections import OrderedDict
from concurrent.futures import ThreadPoolExecutor

from ask_sdk_core.skill_builder import SkillBuilder
from ask_sdk_core.dispatch_components import AbstractRequestHandler
from ask_sdk_core.dispatch_components import AbstractExceptionHandler
import ask_sdk_core.utils as ask_utils
from ask_sdk_core.handler_input import HandlerInput

from ask_sdk_model import Response

logger = logging.getLogger(__name__)
logger.setLevel(logging.INFO)

class LaunchRequestHandler(AbstractRequestHandler):
    """Handler for Skill Launch."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool
        
        return ask_utils.is_request_type("LaunchRequest")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        speak_output = "Welcome, you can say 'all news'. Would you like to try?"

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )


class UKNewsReaderIntentHandler(AbstractRequestHandler):
    """Handler for UKNewsReader Intent."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool
        return ask_utils.is_intent_name("UKNewsReaderIntent")(handler_input)

    def process_one_feed(self, news_feed):
        medium, feed = news_feed

        dehtml = html2text.HTML2Text()
        dehtml.ignore_links = True

        number_of_headlines = 5

        news_list = feedparser.parse(feed)
        speak_output = f" Top {number_of_headlines} news from: {medium}. "
        for i in range(0, number_of_headlines):
            entry = news_list.entries[i]
            speak_output += f" News number {i+1} from {medium}: "
            speak_output += entry.title + ". "
            speak_output += dehtml.handle(entry.summary)

        return speak_output

    def process_all_feeds(self, news_feeds):
        with ThreadPoolExecutor(max_workers=4) as executor:
            return executor.map(self.process_one_feed, news_feeds.items())

    def handle(self, handler_input):
        news_feeds = OrderedDict([
            ("BBC", "http://feeds.bbci.co.uk/news/rss.xml"),
            ("The Guardian", "https://www.theguardian.com/uk/rss"),
            ("The Independent", "https://www.independent.co.uk/rss"),
            ("Metro", "https://www.metro.co.uk/rss"),
        ])
        results = self.process_all_feeds(news_feeds)
        speak_output = ""
        for result in results:
            speak_output += result


        speak_output += " That's all news for you from UK News Reader."

        logger.info(f"{speak_output}")

        return (
            handler_input.response_builder
                .speak(speak_output)
                # .ask("add a reprompt if you want to keep the session open for the user to respond")
                .response
        )


